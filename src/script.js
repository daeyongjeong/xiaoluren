let countdownElement = document.getElementById("countdown");
let animationElement = document.getElementById("animation");
let redLightElement = document.getElementById("red-light");

let countdownValue = 30;
let patternIndex = 0;
let speed = 125; // Initial speed (8 frames per second for slow)

// Initialize grid
for (let i = 0; i < 17 * 17; i++) {
  let pixel = document.createElement("div");
  pixel.classList.add("pixel");
  animationElement.appendChild(pixel);
}

function drawPattern(pattern) {
  let pixels = animationElement.children;
  for (let y = 0; y < pattern.length; y++) {
    for (let x = 0; x < pattern[y].length; x++) {
      let index = y * 17 + x;
      if (pattern[y][x] === 1) {
        pixels[index].classList.add("on");
      } else {
        pixels[index].classList.remove("on");
      }
    }
  }
}

function animate() {
  drawPattern(patterns[patternIndex]);
  patternIndex = (patternIndex + 1) % patterns.length;

  if (speed === 32 && patternIndex % 8 >= 4) {
    for (let pixel of animationElement.children) {
      pixel.classList.remove("on"); // Flicker effect
    }
  }

  anime({
    targets: ".pixel",
    duration: speed,
    easing: "linear",
    complete: animate,
  });
}

function startCountdown() {
  countdownElement.innerText = countdownValue;
  let countdownInterval = setInterval(() => {
    countdownValue--;
    countdownElement.innerText = countdownValue;

    if (countdownValue <= 10) {
      speed = 32; // Fast animation (32 frames per second)
    } else if (countdownValue <= 14) {
      speed = 62.5; // Medium animation (16 frames per second)
    } else {
      speed = 125; // Slow animation (8 frames per second)
    }

    if (countdownValue <= 0) {
      clearInterval(countdownInterval);
      showRedLight();
    }
  }, 1000);
}

function showRedLight() {
  countdownElement.style.display = "none";
  animationElement.style.display = "none";
  redLightElement.style.display = "block";
}

startCountdown();
animate();
